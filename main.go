package main

import "fmt"

func main() {
	var myRune rune = 'a'
	fmt.Println(myRune)

	var myBoolean bool = true
	fmt.Println(myBoolean)

	var intNum3 rune
	fmt.Println(intNum3)

	var myVar string = "Hello World"
	fmt.Println(myVar)

	var1, var2 := 1, 2
	fmt.Println(var1, var2)

	const myConst string = "Hello World"
	fmt.Println(myConst)

	const pi float64 = 3.14159
	fmt.Println(pi)
}
